function entferneElement(arr, wert) {
    // Finde den Index des Elements im Array
    var index = arr.indexOf(wert);
    
    if (index !== -1) {
      arr.splice(index, 1);
    }
  }
  
  // Test
  var meinArray = [1, 2, 3, 4, 5];
  entferneElement(meinArray, 3);
  console.log(meinArray); // Sollte [1, 2, 4, 5] ausgeben
